# download_imagens.py
from urllib.parse import urlparse
from pathlib import Path
import mimetypes
import urllib.request
import ssl

def nome_arquivo_para(url: str, idx: int, content_type: str | None) -> str:
    # tenta pegar do caminho da URL
    caminho = Path(urlparse(url).path)
    nome = caminho.name or f"img_{idx}"

    # garante extensão
    ext = caminho.suffix.lower()
    if not ext and content_type:
        ext = mimetypes.guess_extension(content_type.split(";")[0].strip()) or ""
    if not ext:
        ext = ".jpg"  # fallback
    if not nome.endswith(ext):
        nome = f"{nome}{ext}"

    # remove caracteres inválidos
    nome = "".join(c if c.isalnum() or c in "._- " else "_" for c in nome)
    return nome

def baixar_imagens(urls: list[str], pasta_saida: str = "imagens"):
    saida = Path(pasta_saida)
    saida.mkdir(parents=True, exist_ok=True)

    # contexto SSL que ignora problemas de SNI antigos (opcional)
    ssl_ctx = ssl.create_default_context()

    baixadas = 0
    for i, url in enumerate(urls, start=1):
        try:
            req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
            with urllib.request.urlopen(req, context=ssl_ctx, timeout=30) as resp:
                if resp.status != 200:
                    print(f"[ERRO] {url} -> HTTP {resp.status}")
                    continue
                content_type = resp.headers.get("Content-Type", "")
                if "image" not in content_type:
                    print(f"[IGNORADO] {url} não parece ser imagem ({content_type})")
                    continue

                nome = nome_arquivo_para(url, i, content_type)
                destino = saida / nome
                with open(destino, "wb") as f:
                    f.write(resp.read())
                print(f"[OK] {url} -> {destino}")
                baixadas += 1
        except Exception as e:
            print(f"[ERRO] {url} -> {e}")

    print(f"\nConcluído: {baixadas}/{len(urls)} imagens baixadas. Salvas em: {saida.resolve()}")

